#Default functions of List

# 1.Append function is used for adding something in the list(everytime it adds an element at the very last position)

fruits=["Apple","Orange","Banana","Mango"]
fruits.append("Grapes")
print(fruits)

# 2.Index funtion- for finding out the 

fruits=["Apple","Orange","Banana","Mango"]
In=fruits.index("Banana")
print(In)

# 3.Extend function used for extending the list

fruits=["Apple", "Banana","Mango","Orange"]
Veg=["Potato", "ladyfinger"]
fruits.extend(Veg)
print(fruits)
# 4. Sort function for sorting list in ascending order

fruits=["Apple","Orange","Banana","Mango"]
fruits.sort()
print(fruits)

# 5. Len funtion- For finding out the lenth of list
fruits=["Apple","Orange","Banana","Mango"]
print(len(fruits))


